package homework;

import java.util.ArrayList;

public class ArrayList_3 {
    public static void main(String[] args) {
        ArrayList<String> color = new ArrayList<>();

        color.add("White");
        color.add("Blue");
        color.add("Green");
        color.add("Pink");
        color.add("Sky Blue");

        // For Each loop
        for(String s : color){
            System.out.println(s);
        }





    }
}
